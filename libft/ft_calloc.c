#include "libft.h"
#include <stdlib.h>

void	*ft_calloc(size_t count, size_t size)
{
	void	*res;

	res = malloc(size * count);
	if (res == NULL)
		return (NULL);
	ft_bzero(res, (count * size));
	return (res);
}
